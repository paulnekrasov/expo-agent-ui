describe("generateHealProposals", () => {
  const flow = {
    flow: {
      name: "checkout.happyPath",
      steps: [
        { type: "tap", targetId: "checkout.confirm" },
        { type: "input", targetId: "checkout.shipping.name" },
        { type: "tap", targetId: "checkout.pay" }
      ]
    }
  };

  it("produces proposals for failed steps", () => {
    const results = { failedSteps: [0, 2], errors: ["selector not found", "selector not found"] };
    const heal = generateHealProposals(flow, results);

    expect(heal.proposals).toHaveLength(2);
    expect(heal.proposals[0].originalSelector).toBe("checkout.confirm");
    expect(heal.proposals[1].originalSelector).toBe("checkout.pay");
    expect(heal.failedSteps).toBe(2);
    expect(heal.flowName).toBe("checkout.happyPath");
  });

  it("returns no proposals for passing steps", () => {
    const results = { failedSteps: [], errors: [] };
    const heal = generateHealProposals(flow, results);

    expect(heal.proposals).toHaveLength(0);
    expect(heal.failedSteps).toBe(0);
  });

  it("preserves flow name in result", () => {
    const results = { failedSteps: [1], errors: ["timeout"] };
    const heal = generateHealProposals(flow, results);

    expect(heal.flowName).toBe("checkout.happyPath");
  });

  it("skips steps without targetId", () => {
    const flowNoTarget = {
      flow: {
        name: "test.noTarget",
        steps: [{ type: "navigate", screen: "Home" }]
      }
    };
    const results = { failedSteps: [0], errors: ["navigation failed"] };
    const heal = generateHealProposals(flowNoTarget, results);

    expect(heal.proposals).toHaveLength(0);
  });

  it("provides reason with helpful guidance", () => {
    const results = { failedSteps: [0], errors: ["timeout"] };
    const heal = generateHealProposals(flow, results);

    expect(heal.proposals[0].reason).toContain("inspectTree");
    expect(heal.proposals[0].reason).toContain("checkout.confirm");
    expect(heal.proposals[0].confidence).toBe(0);
    expect(heal.proposals[0].suggestedSelector).toBeUndefined();
  });
});

describe("CLI version", () => {
  const cliPkg = require("../package.json");
  const cliPath = path.resolve(__dirname, "..", "dist", "cli.js");

  it("getAgentUICliManifest returns version matching package.json", () => {
    const manifest = getAgentUICliManifest();
    expect(manifest).toBeDefined();
    expect(manifest.version).toBe(cliPkg.version);
    expect(manifest.packageName).toBe("@agent-ui/cli");
  });

  it("--version flag outputs version string matching package.json", () => {
    const result = spawnSync(process.execPath, [cliPath, "--version"], {
      encoding: "utf8"
    });
    expect(result.status).toBe(0);
    expect(result.stdout.trim()).toBe(cliPkg.version);
    expect(result.stderr).toBe("");
  });

  it("-v short flag outputs version string matching package.json", () => {
    const result = spawnSync(process.execPath, [cliPath, "-v"], {
      encoding: "utf8"
    });
    expect(result.status).toBe(0);
    expect(result.stdout.trim()).toBe(cliPkg.version);
    expect(result.stderr).toBe("");
  });
});
